import os
import shutil
import argparse
from pathlib import Path

def delete_node_modules(root: Path, dry_run: bool = True):
    """
    Recursively find and remove all node_modules directories.
    If dry_run=True, only logs what *would* be deleted.
    """
    found = 0

    # Traverse directory tree
    for dirpath, dirnames, _ in os.walk(root):
        # Create a list since we modify dirnames
        for name in list(dirnames):
            if name == "node_modules":
                nm_path = Path(dirpath) / name
                found += 1
                if dry_run:
                    print(f"[DRY RUN] Found: {nm_path}")
                else:
                    try:
                        print(f"Deleting: {nm_path}")
                        shutil.rmtree(nm_path)
                    except Exception as e:
                        print(f"ERROR deleting {nm_path}: {e}")
                # Prevent walking inside a deleted node_modules dir
                dirnames.remove(name)

    print(f"\nDone. node_modules dirs found: {found}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Fast recursive node_modules deletion script"
    )
    parser.add_argument(
        "path",
        nargs="?",
        default=".",
        help="Root path to start search (default: current directory)",
    )
    parser.add_argument(
        "-f",
        "--force",
        action="store_true",
        help="Actually remove directories (not just dry run)",
    )
    args = parser.parse_args()

    root_path = Path(args.path).resolve()
    if not root_path.is_dir():
        print(f"Error: '{root_path}' is not a directory")
        exit(1)

    dry_run = not args.force
    if dry_run:
        print("Dry run mode (no deletions). Add --force to delete.\n")

    delete_node_modules(root_path, dry_run)