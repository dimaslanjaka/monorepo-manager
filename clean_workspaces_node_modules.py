import os
import argparse
import json
import glob
from pathlib import Path
from typing import Union
import shutil


def delete_path(the_path: Union[Path, str], dry_run: bool = True) -> None:
    """Delete the given path if it exists.
    If `dry_run` is True, only prints what would be deleted.
    """
    path = Path(the_path)

    if not path.exists():
        return

    try:
        prefix = "[DRY RUN] " if dry_run else ""

        if path.is_dir():
            print(f"{prefix}Deleting: {path}")

            # Faster than os.listdir + join
            children = sorted(path.iterdir(), key=lambda p: p.name)

            for child in children:
                print(f"  Deleting: {child}")

                if not dry_run:
                    if child.is_dir():
                        try:
                            shutil.rmtree(child)
                        except Exception as e:
                            print(f"ERROR deleting directory {child}: {e}")
                    else:
                        try:
                            child.unlink()
                        except Exception as e:
                            print(f"ERROR deleting file {child}: {e}")

            if not dry_run:
                try:
                    path.rmdir()  # directory is now empty
                except Exception as e:
                    print(f"ERROR deleting directory {path}: {e}")

        else:
            print(f"{prefix}Deleting file: {path}")

            if not dry_run:
                try:
                    path.unlink()
                except Exception as e:
                    print(f"ERROR deleting file {path}: {e}")

    except Exception as e:
        print(f"ERROR deleting {path}: {e}")


def delete_node_modules(root: Path, dry_run: bool = True):
    """Delete `node_modules` only for package folders defined by
    workspace globs in `package.json`, recursively resolving nested
    workspace package.json files.
    """

    def collect_workspace_dirs(start_root: Path):
        """Return a set of package directories for the workspace rooted at start_root.

        This walks workspace globs declared in each package.json it finds,
        recursively, stopping at directories already visited to avoid cycles.
        """
        visited = set()
        results = set()
        stack = [start_root.resolve()]

        while stack:
            pkg_dir = stack.pop()
            if pkg_dir in visited:
                continue
            visited.add(pkg_dir)

            pkg_file = pkg_dir / "package.json"
            if not pkg_file.is_file():
                continue

            results.add(pkg_dir)

            try:
                data = json.loads(pkg_file.read_text(encoding="utf-8"))
            except Exception:
                continue

            ws_field = data.get("workspaces")
            patterns = []
            if isinstance(ws_field, dict):
                patterns = ws_field.get("packages", [])
            elif isinstance(ws_field, list):
                patterns = ws_field

            for pattern in patterns:
                # Expand pattern relative to the current package directory
                matches = glob.glob(str(pkg_dir / pattern), recursive=True)
                for m in matches:
                    p = Path(m)
                    # Skip symlinked package directories; do not follow them
                    if p.is_symlink():
                        continue
                    p_res = p.resolve()
                    if p_res.is_dir() and p_res not in visited:
                        stack.append(p_res)

        return results

    found = 0
    lock_found = 0
    workspace_dirs = collect_workspace_dirs(root)

    # list of common lock files to remove in each package folder
    lock_files = [
        "yarn.lock",
        "package-lock.json",
        "pnpm-lock.yaml",
        "npm-shrinkwrap.json",
    ]

    # For each workspace package dir, delete its node_modules and lock files if present
    for pkg_dir in sorted(workspace_dirs):
        nm = pkg_dir / "node_modules"
        if nm.exists():
            found += 1
            delete_path(nm, dry_run=dry_run)
        else:
            if dry_run:
                print(f"[SKIP]   No node_modules at: {pkg_dir}")

        # Remove lock files in the package folder
        for lock in lock_files:
            lf = pkg_dir / lock
            if lf.exists():
                lock_found += 1
                delete_path(lf, dry_run=dry_run)
            else:
                if dry_run:
                    # only show skip for lock files during dry run to reduce noise
                    print(f"[SKIP]   No {lock} at: {pkg_dir}")

    print(
        f"\nDone. node_modules dirs considered: {found}; lock files considered: {lock_found}"
    )


def main():
    parser = argparse.ArgumentParser(
        description="Delete node_modules directories recursively from a workspace root, "
        "but only if a package.json exists at that workspace root.",
        epilog="Default workspace: D:\\Repositories",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "path",
        nargs="?",
        default=str(Path.cwd()),
        help=f"Workspace root to operate on (default: {Path.cwd()})",
    )
    parser.add_argument(
        "-f",
        "--force",
        action="store_true",
        help="Actually remove directories (not just dry run)",
    )
    args = parser.parse_args()

    root_path = Path(args.path).resolve()
    if not root_path.is_dir():
        print(f"Error: '{root_path}' is not a directory")
        exit(1)

    package_json = root_path / "package.json"
    if not package_json.is_file():
        print(
            f"Refusing to operate: no package.json found at workspace root '{root_path}'"
        )
        print("Place a package.json at the workspace root or specify a different path.")
        exit(2)

    dry_run = not args.force
    if dry_run:
        print("Dry run mode (no deletions). Add --force to delete.\n")

    delete_node_modules(root_path, dry_run)


if __name__ == "__main__":
    main()
